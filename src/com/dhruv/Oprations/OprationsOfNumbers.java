package com.dhruv.Oprations;

public class OprationsOfNumbers {

	public static void main(String[] args) {
		System.out.println("Java program to print the result of the following operations : +, -, /, *, %");
		System.out.println("Addition of two numbers, 12 + 7 is = "+(7+12));
		System.out.println("Subtraction of two numbers, 12 - 7 is = "+(12-7));
		System.out.println("Division of two numbers, 12 / 7 is = "+(12/7));
		System.out.println("Multiplication of two numbers, 12 * 7 is = "+(7*12));
		System.out.println("Reminder of Divison of two numbers, 12 % 7 is = "+(12%7));
	}

}
